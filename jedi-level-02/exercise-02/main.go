package main

import "fmt"

// Using the following operators, write expressions and assign their values to variables: ==, <=, >=, != , <, >

func main() {
	a := 42 == 42
	b := 42 <= 43
	c := 43 >= 42
	d := 42 != 42
	e := 42 < 43
	f := 43 > 42
	fmt.Println(a)
	fmt.Println(b)
	fmt.Println(c)
	fmt.Println(d)
	fmt.Println(e)
	fmt.Println(f)
}
