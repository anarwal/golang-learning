package main

import "fmt"

// assign variable x as 42, y as "james Bond" and z as true using short declaration operator
// print the variables first as single print statement and then as multiple print statement.

func main() {
	x := 42
	y := "James Bond"
	z := true
	fmt.Println(x, y, z)
	fmt.Println(x)
	fmt.Println(y)
	fmt.Println(z)
}
